package com.java.class15;

public class loop {
    public static void main(String[] args) {
        int result = 0;

        for (int a = 1; a <= 5; a++) {

            result = result + a;

            System.out.println(a);
        }
        System.out.println("=================================");

        System.out.println(result);
    }
}
