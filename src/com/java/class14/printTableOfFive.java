package com.java.class14;

public class printTableOfFive {
    public static void main(String[] args) {
        System.out.println("Printing a multiplying table by 5");

        int a = 0;

        while (a < 10) {
            a++ ;
            System.out.println( a + "*5= " + a * 5 );

        }
    }
}
