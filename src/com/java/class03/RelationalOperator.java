package com.java.class03;

public class RelationalOperator {
    public static void main(String[] args) {

        int a = 10, b = 11;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a != b);
        System.out.println(a == b);


        int c = 10, d = 10;
        System.out.println(c > d);
        System.out.println(c < d);
        System.out.println(c >= d);
        System.out.println(c != d);
        System.out.println(c == d);


    }
}
